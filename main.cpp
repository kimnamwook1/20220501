#include <iostream>

using namespace std;

int main()
{
	int MyGold = 10;

	cout << MyGold + 10 << endl;
	cout << MyGold - 10 << endl;
	cout << MyGold * 10 << endl;
	cout << MyGold / 10 << endl;
	cout << MyGold % 10 << endl;

	float G = 0.1f;

	cout << G + 0.1f << endl;
	cout << G - 0.1f << endl;
	cout << G * 0.1f << endl;
	cout << G / 0.1f << endl; 
	//%연산자는 정수형에서만 사용 가능
}